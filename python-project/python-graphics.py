def re(a,b,c,d,e,f):
    r=Rectangle(a,b,Point(c,d))
    f.add(r)
    r.setFillColor(e)
def pol(x,y,z,a,b,c,d,e,f,g):
    po=Polygon(Point(x,y),Point(z,a),Point(b,c),Point(d,e))
    f.add(po)
    po.setFillColor(g)
def ci(x,y,z,w,u,v):
    c=Circle(x,Point(y,z))
    c.setFillColor(w)
    c.setBorderColor(v)
